/*GOV SCRIPT-------------------------------------------------*/




var govImg = document.getElementById("govImg");
var gov = document.getElementById("gov")


govImg.onmouseover = function(){
  govImg.style.display = "none";
  gov.style.display = "inline-block";
}

gov.onmouseleave = function(){
  govImg.style.display = "block";
  gov.style.display = "none";
}

